package Student_Grade_Management;

public class User 
{
	private long roll_No ;
	private String name ;
	private String dept;
	private int sub1_Mark;
	private int sub2_Mark;
	private int sub3_Mark;
	private int total;
	private String grade;
	public long getRoll_No() {
		return roll_No;
	}
	public void setRoll_No(long roll_No) {
		this.roll_No = roll_No;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public int getSub1_Mark() {
		return sub1_Mark;
	}
	public void setSub1_Mark(int sub1_Mark) {
		this.sub1_Mark = sub1_Mark;
	}
	public int getSub2_Mark() {
		return sub2_Mark;
	}
	public void setSub2_Mark(int sub2_Mark) {
		this.sub2_Mark = sub2_Mark;
	}
	public int getSub3_Mark() {
		return sub3_Mark;
	}
	public void setSub3_Mark(int sub3_Mark) {
		this.sub3_Mark = sub3_Mark;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	

	
}
